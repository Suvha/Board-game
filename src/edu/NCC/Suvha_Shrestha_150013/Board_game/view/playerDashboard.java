    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
      cleartext();
      
       playerDetailList playerDL =new playerDetailList();
        playerDL.setPlayer_location(cmb_location.getSelectedItem().toString());
        playerDL.setPlayer_name(playername.getPlayer_name());
        
           if(txt_timeShot_from.getText().isEmpty() || txt_timeShot_to.getText().isEmpty()){
             JOptionPane.showMessageDialog(null, "Please enter the value", "ERROR", JOptionPane.ERROR_MESSAGE);
         }else{
                   playerDL.setAvailableTime_from(Integer.parseInt(txt_timeShot_from.getText()));
                   
                playerDL.setAvailableTime_to(Integer.parseInt(txt_timeShot_to.getText()));
           }
     try{
       jTextArea_playerList.setText(crudControl.searchplayer(playerDL));
      }catch(ClassNotFoundException | SQLException | NumberFormatException ex ){
         JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",JOptionPane.ERROR_MESSAGE); 
      }
    }//GEN-LAST:event_btn_searchActionPerformed

