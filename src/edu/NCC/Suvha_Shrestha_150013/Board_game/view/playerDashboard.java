   public void fillcomboLocation(){
                   conn.opendbConnection();
           try{
              
                String qry="SELECT * FROM `player_details`";
            PreparedStatement pstmt=conn.initStatement(qry);
           ResultSet res=conn.executeQuery();
            while(res.next()){
                String gameList=res.getString("player_location");
                
                cmb_location.addItem(gameList);
            
            }
            conn.closeCOnnection();
    }
           catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",JOptionPane.ERROR_MESSAGE);
}
          
   }
  public void fillcomboBookmarkList(){
              conn.opendbConnection();
           try{
              
                String qry="SELECT * FROM `bookmark` WHERE player_name='"+playername.getPlayer_name()+"'";
            PreparedStatement pstmt=conn.initStatement(qry);
           ResultSet res=conn.executeQuery();
            while(res.next()){
                String gameList=res.getString("bookmark_name");
                cmb_bookmarkList.addItem(gameList);
            
            }
            conn.closeCOnnection();
    }
           catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",JOptionPane.ERROR_MESSAGE);
  }
  }
